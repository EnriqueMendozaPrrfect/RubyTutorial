# () Parentesis
# / y * División y multiplicación
# + y -	Suma y resta
# La forma en la que se resuelven es de izquierda a derecha

suma = 5 + 6
puts('el valor de la suma es:', suma)

resta = 5 - 2
puts('el valor de la resta es:', resta)

multiplicacion = 3 * 4
puts('el valor de la multiplicacion es:', multiplicacion)

divicion = 12 % 3
puts('el valor de la residuo es:', divicion)

parentesis = (5 + 3) * 2
puts('el valor de la parentesis es:', parentesis)

